local cachedSortedOutKeys = nil

function WoWTranslate_CheckOutGlossaryExact(text)
    local lowerText = string.lower(text)
    if WoWTranslateOutGlossary[lowerText] then
        return WoWTranslateOutGlossary[lowerText]
    end
    return nil
end

function WoWTranslate_CheckOutGlossaryPartial(text)
    if not cachedSortedOutKeys then
        cachedSortedOutKeys = {}
        for k, _ in pairs(WoWTranslateOutGlossary) do
            table.insert(cachedSortedOutKeys, k)
        end
        table.sort(cachedSortedOutKeys, function(a, b) return string.len(a) > string.len(b) end)
    end

    local result = WT_GlossaryPartialMatch(text, WoWTranslateOutGlossary, cachedSortedOutKeys, 4, false)
    return result
end

function WoWTranslate_GetOutGlossaryCount()
    local count = 0
    for _ in pairs(WoWTranslateOutGlossary) do
        count = count + 1
    end
    return count
end
